    private void btSelecionarCamisetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSelecionarCamisetaActionPerformed
        
        String camiseta = (String)txtCamisetas.getSelectedItem();
        
        
        if(camiseta.equals("Camisa Palmeiras")){
           
            valortotal += (50.00) * Double.parseDouble(txtQuantidade.getText());
            
        }else if(camiseta.equals("Camisa São Paulo")){
            valortotal += (40.00) * Double.parseDouble(txtQuantidade.getText());
       
        }else if(camiseta.equals("Camisa Corinthians")){
            valortotal += (80.00) * Double.parseDouble(txtQuantidade.getText());
        }
        String total = String.valueOf(valortotal);
        txtTotalVenda.setText("R$ = " + total);




    }//GEN-LAST:event_btSelecionarCamisetaActionPerformed

