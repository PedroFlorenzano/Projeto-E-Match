    public void readProduto(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos2.getModel();
        modelo.setNumRows(0);
        DAO pdao = new DAO();
        
        for (Produto p : pdao.read()) {
            
            modelo.addRow(new Object[]{
                
                p.getId_produto(),
                p.getNome(),
                p.getDescricao(),
                p.getValor_venda(),
                p.getTamanho_camisa(),
                p.getQuantidade()
                
            });
        }
    }

